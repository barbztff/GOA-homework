"""6) კომენტარებით დაწერეთ რით განსხვავდება integer და float მონაცემთა ტიპები""" 

# Integer (მთელი რიცხვი) და Float (ათწილადი რიცხვი) მონაცემთა ტიპების განსხვავება

# Integer (int) - ეს არის მთელი რიცხვი, რომელიც არ შეიცავს ათწილად ნაწილს
integer_number = 10  # მაგალითად, 10 არის integer

# Float - ეს არის რიცხვი ათწილადი ნაწილით, რომელიც შეიცავს წერტილს (.) ან ექსპონენციალურ ჩანაწერს
float_number = 10.5  # მაგალითად, 10.5 არის float

# ძირითადი განსხვავებები:
# 1. Integer შეიცავს მხოლოდ მთელ რიცხვებს (მაგალითად: -3, 0, 7), ხოლო Float შეიძლება შეიცავდეს ათწილად ნაწილს (მაგალითად: -3.14, 0.0, 7.89)
# 2. Integer ტიპის ცვლადები იყენებენ ნაკლებ მეხსიერებას, Float ტიპის ცვლადები კი - მეტს, რადგან ინახავენ ათწილად ნაწილსაც.